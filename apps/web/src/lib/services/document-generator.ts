const GENERATORS: Record<string, (t: DocumentTemplate, c: DocumentContext) => string> = {
  CONTRACT: generateContract,
  BID: generateBidRequest,
  CHECKLIST: generateChecklist,
  INVOICE: generateInvoice,
  RECEIPT: generateReceipt,
};

export function generateDocument(
  template: DocumentTemplate,
  context: DocumentContext
): string {
  const generator = GENERATORS[template.type] ?? generateGeneric;
  return generator(template, context);
}

export { generateContract, generateBidRequest, generateChecklist, generateInvoice, generateReceipt };
