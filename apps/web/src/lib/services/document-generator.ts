function generateContract(template: DocumentTemplate, ctx: DocumentContext): string {
  const cf = makeCf(ctx);
  const currency = getCurrency(ctx);
  const togo = isTogo(ctx);
  const contractor = ctx.contacts?.find((c) =>
    c.role.toLowerCase().includes("contractor") ||
    c.role.toLowerCase().includes("chef") ||
    c.role.toLowerCase().includes("entreprise")
  );

  let body = headerBlock(template.name, today());

  if (togo) {
    body += `<p class="fr-label">Contrat entre le maitre d'ouvrage et l'entrepreneur</p>`;
  }

  body += `<div class="section">
<h2>1. Parties${togo ? ' <span class="fr-label">(Les parties)</span>' : ""}</h2>
${fieldLine("Owner / Client", cf("Owner name and address") || cf("Maitre d'ouvrage (owner) full name and address") || ctx.project.name, togo ? "Maitre d'ouvrage" : undefined)}
${fieldLine("Contractor", contractor?.name ?? cf("Contractor name, address, and license number") ?? cf("Entrepreneur/chef de chantier full name and address"), togo ? "Entrepreneur" : undefined)}
${contractor?.phone ? fieldLine("Contractor Phone", contractor.phone) : ""}
${contractor?.email ? fieldLine("Contractor Email", contractor.email) : ""}
</div>`;

  body += `<div class="section">
<h2>2. Project Description${togo ? ' <span class="fr-label">(Description du projet)</span>' : ""}</h2>
${fieldLine("Project Name", ctx.project.name)}
${fieldLine("Property Type", ctx.project.propertyType)}
${fieldLine("Size", ctx.project.sizeRange ?? ctx.project.details)}
${fieldLine("Market", ctx.project.market)}
</div>`;

  body += `<div class="section">
<h2>3. Scope of Work${togo ? ' <span class="fr-label">(Etendue des travaux)</span>' : ""}</h2>
<p>${escapeHtml(cf("Scope of work with detailed specifications") || cf("Scope of work (detailed description of all phases included)") || "The Contractor shall furnish all labor, materials, equipment, and services necessary for the complete construction as described in the project plans and specifications.")}</p>
</div>`;

  body += `<div class="section">
<h2>4. Contract Price${togo ? ' <span class="fr-label">(Montant du contrat)</span>' : ""}</h2>
${fieldLine("Total Contract Amount", ctx.project.totalBudget ? formatCurrency(ctx.project.totalBudget, currency) : undefined)}
${fieldLine("Payment Terms", cf("Payment schedule tied to milestones") || cf("Contract price (fixed or cost-plus terms)") || "Per attached payment schedule")}
</div>`;

  // Payment schedule table if budget items available
  if (ctx.budgetItems && ctx.budgetItems.length > 0) {
    body += `<div class="section">
<h2>5. Payment Schedule${togo ? ' <span class="fr-label">(Echeancier de paiement)</span>' : ""}</h2>
<table>
<tr><th>#</th><th>Category${togo ? " / Phase" : ""}</th><th>Estimated Amount</th><th>Status</th></tr>`;
    ctx.budgetItems.forEach((item, i) => {
      body += `<tr>
  <td>${i + 1}</td>
  <td>${escapeHtml(item.category)}</td>
  <td class="amount">${formatCurrency(item.estimated, currency)}</td>
  <td>${escapeHtml(item.status)}</td>
</tr>`;
    });
    body += `<tr class="total-row">
  <td colspan="2">Total</td>
  <td class="amount">${formatCurrency(ctx.budgetItems.reduce((s, b) => s + b.estimated, 0), currency)}</td>
  <td></td>
</tr>`;
    body += `</table></div>`;
  }

  body += `<div class="section">
<h2>${ctx.budgetItems ? "6" : "5"}. Timeline${togo ? ' <span class="fr-label">(Delais)</span>' : ""}</h2>
${fieldLine("Start Date", cf("Start date") || cf("Start date and substantial completion date"), togo ? "Date de debut" : undefined)}
${fieldLine("Estimated Completion", cf("Estimated completion date") || cf("Estimated completion date"), togo ? "Date d'achevement prevue" : undefined)}
${fieldLine("Total Duration", ctx.project.totalWeeks ? `${ctx.project.totalWeeks} weeks` : undefined)}
</div>`;

  body += `<div class="section">
<h2>${ctx.budgetItems ? "7" : "6"}. General Conditions${togo ? ' <span class="fr-label">(Conditions generales)</span>' : ""}</h2>
<p><strong>Change Orders:</strong> Any changes to the scope of work must be documented in writing and signed by both parties before work proceeds.</p>
<p><strong>Insurance:</strong> The Contractor shall maintain adequate general liability and workers compensation insurance throughout the project.</p>
<p><strong>Warranty:</strong> The Contractor warrants all workmanship for a period of ${togo ? "one (1) year from reception provisoire, with garantie decennale for structural defects" : "one (1) year from the date of substantial completion"}.</p>
<p><strong>Dispute Resolution:</strong> ${cf("Dispute resolution method (mediation, arbitration, litigation)") || cf("Dispute resolution mechanism") || "Any disputes arising under this agreement shall be resolved first through good-faith negotiation, then through mediation."}</p>
<p><strong>Termination:</strong> Either party may terminate this agreement with thirty (30) days written notice and payment for all work completed to date.</p>
</div>`;

  body += signatureLines(
    togo ? "Maitre d'ouvrage (Owner)" : "Owner",
    togo ? "Entrepreneur (Contractor)" : "Contractor",
    "Witness"
  );

  body += disclaimerBlock();

  return wrapHtml(template.name, body);
}

