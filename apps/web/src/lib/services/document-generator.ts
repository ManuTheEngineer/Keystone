function wrapHtml(title: string, bodyHtml: string): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>${escapeHtml(title)}</title>
<style>${BASE_STYLES}${PRINT_STYLES}</style>
</head>
<body>
${bodyHtml}
</body>
</html>`;
}

function escapeHtml(str: string): string {
  return str
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function headerBlock(title: string, date: string): string {
  return `<div class="header-meta">
  <div class="brand">Keystone</div>
  <div class="date">${escapeHtml(date)}</div>
</div>
<h1>${escapeHtml(title)}</h1>`;
}

function disclaimerBlock(): string {
  return `<div class="disclaimer">
  Generated by Keystone. This document is a template and should be reviewed by a qualified professional before use.
</div>`;
}

function signatureLines(...labels: string[]): string {
  const lines = labels
    .map(
      (l) =>
        `<div class="signature-line">${escapeHtml(l)}<br><span style="font-size:8pt;color:#6A6A6A;">Date: <span class="blank-short">&nbsp;</span></span></div>`
    )
    .join("\n    ");
  return `<div class="signature-block">\n    ${lines}\n</div>`;
}

function fieldLine(label: string, value: string | undefined, frLabel?: string): string {
  const display = value && value.trim() ? escapeHtml(value) : '<span class="blank">&nbsp;</span>';
  const fr = frLabel ? ` <span class="fr-label">(${escapeHtml(frLabel)})</span>` : "";
  return `<div class="field-row"><span class="field-label">${escapeHtml(label)}${fr}:</span> <span class="field-value">${display}</span></div>`;
}

function formatCurrency(amount: number, currency: string): string {
  if (currency === "XOF" || currency === "CFA") {
    return new Intl.NumberFormat("fr-FR", { style: "decimal", maximumFractionDigits: 0 }).format(amount) + " FCFA";
  }
  if (currency === "GHS") {
    return "GH\u20B5" + new Intl.NumberFormat("en-GH", { style: "decimal", minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(amount);
  }
  return "$" + new Intl.NumberFormat("en-US", { style: "decimal", minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(amount);
}

function today(): string {
  return new Date().toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" });
}

function isTogo(ctx: DocumentContext): boolean {
  return ctx.project.market === "TOGO" || ctx.project.market === "BENIN";
}

function getCurrency(ctx: DocumentContext): string {
  return ctx.project.currency ?? (isTogo(ctx) ? "XOF" : "USD");
}

function makeCf(ctx: DocumentContext): (label: string) => string {
  return (label: string) => ctx.customFields?.[label] ?? "";
}

