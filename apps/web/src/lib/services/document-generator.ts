// ---------------------------------------------------------------------------

function generateInvoice(template: DocumentTemplate, ctx: DocumentContext): string {
  const cf = makeCf(ctx);
  const currency = getCurrency(ctx);
  const togo = isTogo(ctx);

  let body = headerBlock(template.name, today());

  if (togo) {
    body += `<p class="fr-label">Demande de decaissement / Facture</p>`;
  }

  body += `<div class="section">
${fieldLine("Draw / Invoice Number", cf("Draw request number") || cf("Order number and date"))}
${fieldLine("Project Name", ctx.project.name)}
${fieldLine("Date", today())}
</div>`;

  body += `<div class="section">
<h2>Parties${togo ? ' <span class="fr-label">(Les parties)</span>' : ""}</h2>
${fieldLine(togo ? "Owner (Maitre d'ouvrage)" : "Borrower / Owner", cf("Borrower name") || cf("Payer (maitre d'ouvrage or representative) name"))}
${fieldLine("Contractor", cf("Contractor name") || ctx.contacts?.find((c) => c.role.toLowerCase().includes("contractor"))?.name)}
${!togo ? fieldLine("Loan Number", cf("Loan number")) : ""}
</div>`;

  body += `<div class="section">
<h2>Cost Breakdown${togo ? ' <span class="fr-label">(Detail des couts)</span>' : ""}</h2>`;

  if (ctx.budgetItems && ctx.budgetItems.length > 0) {
    body += `<table>
<tr><th>Category</th><th>Estimated</th><th>Spent to Date</th><th>This Draw</th><th>Remaining</th></tr>`;
    let totalEstimated = 0;
    let totalSpent = 0;
    ctx.budgetItems.forEach((item) => {
      totalEstimated += item.estimated;
      totalSpent += item.actual;
      const remaining = item.estimated - item.actual;
      body += `<tr>
  <td>${escapeHtml(item.category)}</td>
  <td class="amount">${formatCurrency(item.estimated, currency)}</td>
  <td class="amount">${formatCurrency(item.actual, currency)}</td>
  <td class="amount"><span class="blank-short">&nbsp;</span></td>
  <td class="amount">${formatCurrency(remaining > 0 ? remaining : 0, currency)}</td>
</tr>`;
    });
    body += `<tr class="total-row">
  <td>Total</td>
  <td class="amount">${formatCurrency(totalEstimated, currency)}</td>
  <td class="amount">${formatCurrency(totalSpent, currency)}</td>
  <td class="amount"><span class="blank-short">&nbsp;</span></td>
  <td class="amount">${formatCurrency(totalEstimated - totalSpent > 0 ? totalEstimated - totalSpent : 0, currency)}</td>
</tr>`;
    body += `</table>`;
  } else {
    body += `<p>${escapeHtml(cf("Itemized cost breakdown by category") || "See attached itemized breakdown.")}</p>`;
  }

  body += `</div>`;

  body += `<div class="section">
<h2>Summary${togo ? ' <span class="fr-label">(Resume)</span>' : ""}</h2>
${fieldLine("Total Budget", ctx.project.totalBudget ? formatCurrency(ctx.project.totalBudget, currency) : undefined)}
${fieldLine("Previously Disbursed", ctx.project.totalSpent ? formatCurrency(ctx.project.totalSpent, currency) : cf("Amount previously disbursed"))}
${fieldLine("Amount Requested", cf("Amount requested this draw") || cf("Amount paid in CFA"))}
${fieldLine("Completion Percentage", ctx.project.progress ? `${ctx.project.progress}%` : cf("Percentage of overall completion"))}
</div>`;

  body += signatureLines(
    togo ? "Maitre d'ouvrage (Owner)" : "Owner / Borrower",
    togo ? "Entrepreneur (Contractor)" : "Contractor"
  );

  body += disclaimerBlock();

  return wrapHtml(template.name, body);
}

